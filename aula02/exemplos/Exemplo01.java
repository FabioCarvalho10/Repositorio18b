package exemplos;

public class Exemplo01 {

    public static void main(String[] args) {
        int valor;

        valor = -10;

        if (valor > 0) {
            System.out.println(valor + " é um número positivo.");
        } else {
            System.out.println(valor + " é um número negativo.");
        }

        System.out.println("Final do programa.");
    }
}